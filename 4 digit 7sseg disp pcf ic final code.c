static const uint8_t seg_map[4][8] = {
    { D1_A, D1_B, D1_C, D1_D, D1_E, D1_F, D1_G, D1_DP },
    { D2_A, D2_B, D2_C, D2_D, D2_E, D2_F, D2_G, D2_DP },
    { D3_A, D3_B, D3_C, D3_D, D3_E, D3_F, D3_G, D3_DP },
    { D4_A, D4_B, D4_C, D4_D, D4_E, D4_F, D4_G, D4_DP },
};

static const uint8_t digit_en[4] = {
    D1_ENABLE, D2_ENABLE, D3_ENABLE, D4_ENABLE
};

static uint8_t lcd_ram[4];

// ?? I2C ???????????????????????????????????????????????????????????????????
static void twi0_init(void)
{
    PORTA.DIRCLR   = PIN2_bm | PIN3_bm;
    PORTA.PIN2CTRL = PORT_PULLUPEN_bm;
    PORTA.PIN3CTRL = PORT_PULLUPEN_bm;
    TWI0.MBAUD     = 0x0C;
    TWI0.MCTRLA    = TWI_ENABLE_bm;
    TWI0.MSTATUS   = TWI_BUSSTATE_IDLE_gc;
}

static bool twi_wait(void)
{
    while (!(TWI0.MSTATUS & TWI_WIF_bm));
    return !(TWI0.MSTATUS & TWI_RXACK_bm);
}

static bool twi_start(uint8_t addr)
{
    TWI0.MADDR = (addr << 1);
    return twi_wait();
}

static void twi_write(uint8_t data)
{
    TWI0.MDATA = data;
    twi_wait();
}

static void twi_stop(void)
{
    TWI0.MCTRLB = TWI_MCMD_STOP_gc;
}

static void lcd_send(uint8_t *data, uint8_t len)
{
    if (!twi_start(PCF85176_ADDR_7BIT)) return;
    for (uint8_t i = 0; i < len; i++)
        twi_write(data[i]);
    twi_stop();
}

static void lcd_init(void)
{
    _delay_ms(5);
    uint8_t seq[] = {
        PCF_CMD_MODE_STATIC,
        PCF_CMD_LOAD_PTR_0,
        0x00, 0x00, 0x00, 0x00
    };
    lcd_send(seq, sizeof(seq));
}

static void lcd_clear(void)
{
    lcd_ram[0] = 0;
    lcd_ram[1] = 0;
    lcd_ram[2] = 0;
    lcd_ram[3] = 0;
}

static void lcd_set(uint8_t s, bool on)
{
    uint8_t byte = s / 8;
    uint8_t bit  = 7 - (s % 8);
    if (on)
        lcd_ram[byte] |=  (1 << bit);
    else
        lcd_ram[byte] &= ~(1 << bit);
}

static void lcd_update(void)
{
    uint8_t seq[] = {
        PCF_CMD_LOAD_PTR_0,
        lcd_ram[0], lcd_ram[1],
        lcd_ram[2], lcd_ram[3]
    };
    lcd_send(seq, sizeof(seq));
}

// ?? Raw byte to one digit ?????????????????????????????????????????????????
// pos  = 0,1,2,3 (left to right)
// data = 8-bit pattern:
//        bit7=a  bit6=b  bit5=c  bit4=d
//        bit3=e  bit2=f  bit1=g  bit0=dp
static void lcd_byte_to_digit(uint8_t pos, uint8_t data)
{
    if (!digit_en[pos]) return;

    for (uint8_t i = 0; i < 8; i++)
    {
        bool on = (data >> (7 - i)) & 0x01;
        lcd_set(seg_map[pos][i], on);
    }
}

// ?? Main ??????????????????????????????????????????????????????????????????
int main(void)
{
    twi0_init();
    lcd_init();

    lcd_clear();

    // Change these 4 bytes to whatever pattern you need
    // bit7=a  bit6=b  bit5=c  bit4=d  bit3=e  bit2=f  bit1=g  bit0=dp
    lcd_byte_to_digit(0, 0xFF);   // digit 1
    lcd_byte_to_digit(1, 0xFF);   // digit 2
    lcd_byte_to_digit(2, 0xEC);   // digit 3
    lcd_byte_to_digit(3, 0x7C);   // digit 4

    lcd_update();

    while (1);   // hold display forever
}